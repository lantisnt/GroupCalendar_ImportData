function SafePullClassic_Options_Save()
  SafePullClassic_Log("SafePullClassic_Options_Save")
  -- Validation
  
  -- All Dropdown inputs are autogenerated thus not needing validation
  -- This is ofcourse naive as Options are Global and other addons can touch it.

  -- Sanity
  if SafePullClassic_Options_Temporary.Values.Health == 0 then 
    SafePullClassic_Options_Temporary.Values.Health = SafePullClassic_Options_Defaults.Values.Health
  end
  
  if SafePullClassic_Options_Temporary.Values.Mana == 0 then 
    SafePullClassic_Options_Temporary.Values.Mana = SafePullClassic_Options_Defaults.Values.Mana
  end
  
  if SafePullClassic_Options_Temporary.Values.Refresh == 0 then 
    SafePullClassic_Options_Temporary.Values.Refresh = SafePullClassic_Options_Defaults.Values.Refresh
  end
  
  -- Saturation
  if SafePullClassic_Options_Temporary.Values.Health < 0 then SafePullClassic_Options_Temporary.Values.Health = 0 end
  if SafePullClassic_Options_Temporary.Values.Health > 1 then SafePullClassic_Options_Temporary.Values.Health = 1 end
  -- Saturation
  if SafePullClassic_Options_Temporary.Values.Mana < 0 then SafePullClassic_Options_Temporary.Values.Mana = 0 end
  if SafePullClassic_Options_Temporary.Values.Mana > 1 then SafePullClassic_Options_Temporary.Values.Mana = 1 end
  -- Min limit
  if SafePullClassic_Options_Temporary.Values.Refresh < 0.1 then SafePullClassic_Options_Temporary.Values.Refresh = 0.1 end
  
  -- Save
  SafePullClassic_Options = SafePullClassic_Options_Temporary
end

function SafePullClassic_Options_Load()
  SafePullClassic_Log("SafePullClassic_Options_Load")
  SafePullClassic_Options_Temporary = table.clone(SafePullClassic_Options)
end

