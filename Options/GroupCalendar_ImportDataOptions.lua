  local SafePullClassic_Options_Defaults = { -- Defaults
    Modes = {
      Safety = 1, -- Mana
      Classification = {
        Health  = 0, -- Disabled
        Mana = 1 -- Average
      }
    },
    Values = {
      Mana = 0.6,
      Health = 0.75,
      Refresh = 3
    }
}

function SafePullClassic_Options_Init()
  SafePullClassic_Log("SafePullClassic_Options_Init")
  
  if not SafePullClassic_Options then -- SavedVariable
    SafePullClassic_Options = {
      Modes = {
        Safety = SafePullClassic_Options_Defaults.Modes.Safety,
        Classification = {
          Health  = SafePullClassic_Options_Defaults.Modes.Classification.Health,
          Mana = SafePullClassic_Options_Defaults.Modes.Classification.Mana
        }
      },
      Values = {
        Mana = SafePullClassic_Options_Defaults.Values.Mana,
        Health = SafePullClassic_Options_Defaults.Values.Health,
        Refresh = SafePullClassic_Options_Defaults.Values.Refresh
      }
    }
  end
  
  SafePullClassic_Options_Temporary = table.clone(SafePullClassic_Options)
  
end

