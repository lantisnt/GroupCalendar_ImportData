-- Utility 
function SafePullClassic_Options_GetSafetyModeSplit()
  SafePullClassic_Log("SafePullClassic_Options_GetSafetyModeSplit")
  local health, mana;
  if SafePullClassic_Options.Modes.Safety == SafePullClassic_Constants.SAFETY_MODES.Mana then
    health = false; mana = true;
  elseif SafePullClassic_Options.Modes.Safety == SafePullClassic_Constants.SAFETY_MODES.Health then
    health = true; mana = false;
  elseif SafePullClassic_Options.Modes.Safety == SafePullClassic_Constants.SAFETY_MODES.Both then
    health = true; mana = true;
  else
    health = false; mana = false;
  end
  
  return health, mana
      
end

